print("\n -------------------------------- This is the Program to Calculate the Square of a Number --------------------------------")

def main():
    num : float = float(input("\n\t Type a number to see its square: "))
    square = num * num
    
    print(f"\n\n\t {num:.1f} squared is {square:.1f}")
if __name__ == '__main__':
    main()
    
print("\n -------------------------------- Thank you for using the Program --------------------------------")